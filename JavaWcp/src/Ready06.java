
public class Ready06 {
 public static void main(String args[]) {
	 int a = 15;
	 int c;
	 c = a + 3;  // c = 18
	 System.out.println(c = a + 3);
	 
	 c = a - 5;  // c = 10
	 System.out.println(c = a -5 );
	 
	 c = a * 2;  // c = 30
	 System.out.println(c = a * 2);
	 
	 c = a / 6;  // c = 2
	 System.out.println(c = a / 6);
	 
	 c = a % 4;  // c = 3
	 System.out.println(c = a % 4);
	 
	 
	 
	 
	 int b;
	 b = 10;
	 b++;  // a = 11
	 b = 10;
	 b--;  // a = 9
 }
}
